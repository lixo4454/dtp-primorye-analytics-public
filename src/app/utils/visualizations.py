def fmt_int(n: int | float) -> str:
    """Форматирование целых: 29413 → '29 413' (узкий неразрывный пробел)."""
    return f"{int(n):,}".replace(",", " ")


def color_by_pct_dead(pct: float) -> str:
    """Цвет круга на карте очагов по доле смертельных ДТП.

    Пороги — на основе распределения ``pct_dead`` в hotspots_summary
    (median ≈ 0.025, p90 ≈ 0.05).
    """
    if pct >= 0.05:
        return "#c0392b"  # ≥ 5% — красный
    if pct >= 0.03:
        return "#e67e22"  # 3–5% — оранжевый
    if pct >= 0.02:
        return "#f39c12"  # 2–3% — жёлтый
    return "#3498db"  # < 2% — синий
